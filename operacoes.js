var operacoes = {
    mais : (numA, numB) => {
        return (numA + numB)
    },
    menos: (numA, numB) => {
        return (numA - numB)
    },
    vezes: (numA, numB) => {
        return (numA * numB)
    },
    divisao: (numA, numB) => {
        return (numA / numB)
    },  
}
module.exports = operacoes